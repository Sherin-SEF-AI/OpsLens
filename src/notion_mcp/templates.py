def runbook_content(
    title: str,
    service: str,
    category: str,
    trigger_conditions: str,
    steps: list[str],
    rollback_steps: list[str] | None = None,
    notes: str = "",
) -> str:
    """Generate runbook page Markdown content."""
    steps_md = "\n".join(f"{i+1}. {step}" for i, step in enumerate(steps))
    rollback_md = ""
    if rollback_steps:
        rollback_md = "## Rollback Procedure\n" + "\n".join(
            f"{i+1}. {step}" for i, step in enumerate(rollback_steps)
        )

    return f"""## Overview
**Service:** {service}
**Category:** {category}
**Trigger Conditions:** {trigger_conditions}

---

## Remediation Steps
{steps_md}

---

{rollback_md}

{f"## Notes{chr(10)}{notes}" if notes else ""}
"""
