"""Markdown content templates for Notion pages."""

from datetime import datetime


def incident_page_content(
    incident_id: str,
    title: str,
    severity: str,
    service: str,
    source: str,
    description: str,
    triggered_at: str,
    source_url: str = "",
    dashboard_url: str = "",
    runbook_url: str = "",
    labels: dict[str, str] | None = None,
) -> str:
    """Generate the initial Markdown content for an incident page."""
    links_section = ""
    if source_url:
        links_section += f"- [Alert Source]({source_url})\n"
    if dashboard_url:
        links_section += f"- [Dashboard]({dashboard_url})\n"
    if runbook_url:
        links_section += f"- [Runbook]({runbook_url})\n"

    labels_section = ""
    if labels:
        labels_section = "\n".join(f"- **{k}:** {v}" for k, v in labels.items())

    return f"""## Incident Details

**ID:** {incident_id}
**Severity:** {severity}
**Service:** {service}
**Source:** {source}
**Triggered:** {triggered_at}

### Description
{description}

### Links
{links_section if links_section else "_No links available_"}

### Labels
{labels_section if labels_section else "_No labels_"}

---

## Timeline
_Timeline events are added as comments on this page._

---

## Agent Analysis
_AI agents will post their analysis as comments below._
"""


